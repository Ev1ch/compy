/**
 * Enum for color codes, used
 * to colorize the output of the logger.
 * The color allows logs to be more readable and
 * distinguishable.
 */
enum Color {
  RED = 'red',
  GREEN = 'green',
  YELLOW = 'yellow',
  BLUE = 'blue',
  MAGENTA = 'magenta',
  CYAN = 'cyan',
  WHITE = 'white',
  BLACK = 'black',
  GRAY = 'gray',
}

export default Color;
